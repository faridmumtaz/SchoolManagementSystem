    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(jTable1.getSelectedRow() > -1){
            String command = "SELECT subject_name, score FROM score WHERE student_id = " + jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            ResultSet rs = new DataAccessLayer().selectRecords(command);

            try{
            rs.last();
            int totalrow = rs.getRow();
            rs.beforeFirst();
            String[] columns2 = {"Subject","Score"};
            Object[][] data2 = new Object[totalrow][columns2.length];
            int y = 0;
            while(rs.next()){
                data2[y][0] = rs.getString(1);
                data2[y][1] = rs.getString(2);
                y++;
            }
            jTable2 = new JTable(data2,columns2);
            jScrollPane2.setViewportView(jTable2);
            jTable2.setFont(new Font("Serif", Font.PLAIN, 20));
            jTable2.setRowHeight(40);
            jLabel5.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1) + "`s score sheet");
            }
            catch(SQLException e){
                System.out.println(e.getMessage());
            }
        }
        else{
            javax.swing.JOptionPane.showMessageDialog(this, "Select a record first");
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

