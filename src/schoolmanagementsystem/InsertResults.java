    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        
        String command = "SELECT * FROM student WHERE class = '" + jComboBox1.getSelectedItem() +"'";
        ResultSet rs = new DataAccessLayer().selectRecords(command);
        try{
        rs.last();
        int totalrows = rs.getRow();
        rs.beforeFirst();
        
        
        String columns[] = {"ID","First Name","Last Name","Class"};
        
        
        data = new Object[totalrows][columns.length];
        int y = 0;
        while(rs.next()){
            data[y][0] = rs.getInt(1);
            data[y][1] = rs.getString(2);
            data[y][2] = rs.getString(3);
            data[y][3] = rs.getString(6);
            y++;
        }
        
        jTable1 = new JTable(data,columns);
        jScrollPane1.setViewportView(jTable1);
        jTable1.setFont(new Font("Serif", Font.PLAIN, 20));
        jTable1.setRowHeight(40);
        
        //populating jcombobox2 with appropriate items taken from database
        command = "SELECT subject FROM grade, subject, grade_subject WHERE subject.subject_id=grade_subject.subject_id and grade.grade_id=grade_subject.grade_id and grade= '"+ jComboBox1.getSelectedItem() +"'";
        rs = new DataAccessLayer().selectRecords(command);
        rs.last();
        int rownum = rs.getRow();
        int i = 0;
        rs.beforeFirst();
        String subjects[] = new String[rownum];
        while(rs.next()){
            subjects[i++] = rs.getString(1);
        }
        jComboBox2.setModel(new javax.swing.DefaultComboBoxModel<>(subjects));
        }
        catch(SQLException e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTable1.getSelectedRow()>-1){
            jPanel1.setVisible(true);
            if(jComboBox2.getSelectedItem() == null){
                javax.swing.JOptionPane.showMessageDialog(this, "Please select the subject");
            }
            else{
                int id = (int)jTable1.getValueAt(jTable1.getSelectedRow(), 0);
                String command = "SELECT firstname FROM student WHERE student_id = " + id;
                ResultSet rs = new DataAccessLayer().selectRecords(command);
                try{
                rs.next();
                jLabel3.setText(rs.getString(1)+"`s "+jComboBox2.getSelectedItem()+" Score: ");
                }
                catch(SQLException e){
                    System.out.println("SQL Exception");
                }
            }
        }
        else{
            javax.swing.JOptionPane.showMessageDialog(this, "Select a record first");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

