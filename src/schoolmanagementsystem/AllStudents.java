    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jTable1.getSelectedRow()>-1){
            jPanel1.setVisible(true);
            int id = (int)jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            try{
                ResultSet rs = new DataAccessLayer().selectRecords("SELECT * FROM student WHERE student_id =" + id);
                rs.next();
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(6));
                jTextArea1.setText(rs.getString(7));
            }
            catch(SQLException e){
                javax.swing.JOptionPane.showMessageDialog(this, "Unable to fetch Record");
            }
        }
        else{
            javax.swing.JOptionPane.showMessageDialog(this, "No record selected");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(jTable1.getSelectedRow()>-1){
            int id = (int)jTable1.getValueAt(jTable1.getSelectedRow(), 0);
                int result = new DataAccessLayer().deleteRecords("student", id);
                if(result == 1){
                    formWindowOpened(null);
                    javax.swing.JOptionPane.showMessageDialog(this, "Record Deleted Successfully");
                }
                else{
                    javax.swing.JOptionPane.showMessageDialog(this, "Unable to Delete Record");
                }
        }
        else{
            javax.swing.JOptionPane.showMessageDialog(this, "No Record Selected");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String firstname = jTextField2.getText();
        String lastname = jTextField3.getText();
        String clss = jTextField4.getText();
        String address = jTextArea1.getText();
        int id = (int)jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            int result = new DataAccessLayer().updateRecords("UPDATE student SET firstname='"+ firstname +"', lastname='" + lastname +"', class='"+ clss +"', address='"+ address +"' WHERE student_id = "+ id);
            if(result == 1){
                jPanel1.setVisible(false);
                formWindowOpened(null);
                javax.swing.JOptionPane.showMessageDialog(this, "Record Updated");
            }
            else{
                javax.swing.JOptionPane.showMessageDialog(this, "Some error occured"+id);
            }
    }//GEN-LAST:event_jButton4ActionPerformed

