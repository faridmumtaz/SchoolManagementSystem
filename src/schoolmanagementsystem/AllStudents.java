    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try{
        ResultSet rs = new DataAccessLayer().selectRecords("SELECT * FROM student ORDER BY student_id");
        rs.last();
        int totalrows = rs.getRow();
        rs.beforeFirst();
        String columns[] = {"ID","First Name","Last Name","DOB","Class","Address"};
        Object[][] data = new Object[totalrows][6];
        int y = 0;
        while(rs.next()){
            data[y][0] = rs.getInt(1);
            data[y][1] = rs.getString(2);
            data[y][2] = rs.getString(3);
            data[y][3] = rs.getString(4);
            data[y][4] = rs.getString(6);
            data[y][5] = rs.getString(7);
            y++;
        }
        jTable1 = new JTable(data,columns);
        jScrollPane1.setViewportView(jTable1);
        jTable1.setFont(new Font("Serif", Font.PLAIN, 20));
        jTable1.setRowHeight(40);
        }
        catch(SQLException e){
            javax.swing.JOptionPane.showMessageDialog(this, "Unable to retrieve records");
        }
    }//GEN-LAST:event_formWindowOpened

